export const normalizeProduct = (product) => {
  const isVitaminCSerum = product.name === 'GlowCare Vitamin C Serum';
  const img = product.imageUrl || product.image;
  const desc = product.fullDescription || product.description || product.shortDescription;

  return {
    ...product,
    id: product.id || product._id,
    _id: product._id || product.id,
    imageUrl: isVitaminCSerum ? vitaminCSerumImage : img,
    image: isVitaminCSerum ? vitaminCSerumImage : img,
    rating: product.rating || 4.6,
    shortDescription: product.shortDescription || '',
    description: desc,
    fullDescription: desc,
    benefits: product.benefits?.length ? product.benefits : ['Premium quality', 'Thoughtful daily care', 'GlowCare approved'],
    stock: typeof product.stock === 'number' ? product.stock : 20,
    featured: Boolean(product.featured),
  };
};

export const mergeProductData = (apiProducts = []) => {
  const normalizedApiProducts = apiProducts.map(normalizeProduct);
  const productMap = new Map();

  glowcareProducts.map(normalizeProduct).forEach((product) => {
    productMap.set(product._id, product);
  });

  normalizedApiProducts.forEach((product) => {
    productMap.set(product._id, product);
  });

  return Array.from(productMap.values());
};

export const findGlowcareProduct = (id) =>
  glowcareProducts.map(normalizeProduct).find((product) => product._id === id || product.id === id);
